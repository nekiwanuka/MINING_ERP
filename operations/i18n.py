def normalize_language(language):
    return language if language in {"en", "fr", "zh"} else "en"


def translate(text, language="en"):
    language = normalize_language(language)
    if language == "en":
        return str(text)
    return TRANSLATIONS.get(language, {}).get(str(text), str(text))
